const translatedNodes = new WeakMap<Node, string>();
let observer: MutationObserver | null = null;
let activeLanguage: LanguageCode = 'en';
let scheduled = false;

function normalize(text: string) {
  return text.replace(/\s+/g, ' ').trim();
}

export function tDom(text: string, language: LanguageCode) {
  if (language === 'en') return text;
  const trimmed = normalize(text);
  if (!trimmed) return text;
  const exact = dictionaries[language]?.[trimmed];
  if (exact) return preserveOuterWhitespace(text, exact);
  const compound = translateCompoundText(text, language);
  return compound || text;
}

function preserveOuterWhitespace(original: string, translated: string) {
  const leading = original.match(/^\s*/)?.[0] || '';
  const trailing = original.match(/\s*$/)?.[0] || '';
  return `${leading}${translated}${trailing}`;
}

function translationEntries(language: LanguageCode) {
  return Object.entries(dictionaries[language] || {})
    .filter(([source, target]) => source && target && source.length >= 3)
    .sort((a, b) => b[0].length - a[0].length);
}

function translateCompoundText(text: string, language: LanguageCode) {
  let out = text;
  let changed = false;
  for (const [source, target] of translationEntries(language)) {
    if (!source || source === target || !out.includes(source)) continue;
    out = out.split(source).join(target);
    changed = true;
  }
  return changed ? out : '';
}

function getOriginalText(node: Text) {
  const current = node.nodeValue || '';
  const stored = translatedNodes.get(node);
  if (stored) return stored;
  const normalized = normalize(current);
  for (const dict of Object.values(dictionaries)) {
    for (const [source, translated] of Object.entries(dict)) {
      if (normalize(translated) === normalized) return source;
    }
  }
  return current;
}

function translateTextNode(node: Text, language: LanguageCode) {
  const original = getOriginalText(node);
  const next = tDom(original, language);
  if (next !== node.nodeValue) node.nodeValue = next;
  translatedNodes.set(node, original);
}

function getOriginalAttr(element: Element, attr: string) {
  const originalAttr = `data-i18n-original-${attr}`;
  const current = element.getAttribute(attr) || '';
  const stored = element.getAttribute(originalAttr);
  if (stored) return stored;
  const normalized = normalize(current);
  for (const dict of Object.values(dictionaries)) {
    for (const [source, translated] of Object.entries(dict)) {
      if (normalize(translated) === normalized) return source;
    }
  }
  return current;
}

function translateElementAttributes(element: Element, language: LanguageCode) {
  for (const attr of ['placeholder', 'aria-label', 'title']) {
    const originalAttr = `data-i18n-original-${attr}`;
    const original = getOriginalAttr(element, attr);
    if (!original) continue;
    element.setAttribute(originalAttr, original);
    element.setAttribute(attr, tDom(original, language));
  }
}
function walk(root: ParentNode, language: LanguageCode) {
  const walker = document.createTreeWalker(root, NodeFilter.SHOW_TEXT, {
    acceptNode(node) {
      const parent = node.parentElement;
      if (!parent) return NodeFilter.FILTER_REJECT;
      const tag = parent.tagName.toLowerCase();
      if (['script', 'style', 'code', 'pre', 'textarea'].includes(tag)) return NodeFilter.FILTER_REJECT;
      if (!normalize(node.nodeValue || '')) return NodeFilter.FILTER_REJECT;
      return NodeFilter.FILTER_ACCEPT;
    },
  });
  const textNodes: Text[] = [];
  while (walker.nextNode()) textNodes.push(walker.currentNode as Text);
  textNodes.forEach((node) => translateTextNode(node, language));
  if (root instanceof Element) translateElementAttributes(root, language);
  root.querySelectorAll?.('[placeholder], [aria-label], [title]').forEach((element) => translateElementAttributes(element, language));
}

function scheduleTranslate() {
  if (scheduled) return;
  scheduled = true;
  window.requestAnimationFrame(() => {
    scheduled = false;
    walk(document.body, activeLanguage);
  });
}

export function applyDomTranslations(language: LanguageCode) {
  activeLanguage = language;
  if (typeof window === 'undefined' || !document.body) return () => undefined;
  walk(document.body, language);
  observer?.disconnect();
  observer = new MutationObserver(scheduleTranslate);
  observer.observe(document.body, { childList: true, subtree: true, characterData: true, attributes: true, attributeFilter: ['placeholder', 'aria-label', 'title'] });
  return () => observer?.disconnect();
}
